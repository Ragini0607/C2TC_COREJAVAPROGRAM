package day2;

public class ForLoopExample 
{

	public static void main(String[] args)
	{
		for(int i=1; i<=50 ; i++)    
		{	
			System.out.print("Value of i:");
			System.out.println(i);
		}
		 
		for(int j=30; j>=1;j--)
		{	
			System.out.print("Value of j = ");
			System.out.println( j);
		}
	}

}
